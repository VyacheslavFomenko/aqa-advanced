console.log("For loop");
const multipleNumber = 5;
let m;
for (i = 1; i <= 10; i++) {
    m = multipleNumber * i;
    console.log(`${multipleNumber} * ${i} = ${m}`);
}
//with while
console.log("While loop");
i = 1
while (i<=10){
    m = multipleNumber * i;
    console.log(`${multipleNumber} * ${i} = ${m}`);
    i++;
}